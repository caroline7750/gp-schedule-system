abstract class Person {
    
    private String name;
    private int ID;
    
    private String getName()
    {
        return this.name;
    }
    
    private void setName(String name)
    {
        this.name = name;
    } 

    public int getID() 
    {
        return ID;
    }

    public void setID(int ID) 
    {
        this.ID = ID;
    }
        
}
